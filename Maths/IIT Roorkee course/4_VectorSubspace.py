# Vector Subspace:

# A vector subspace is a subset of a vector space that is itself a vector space. It is a collection of vectors that satisfies the two main properties of a vector space: closure under vector addition and closure under scalar multiplication.

# Properties of Vector Subspace:

# Closure under Vector Addition:

# If vectors u and v are in the subspace, then their sum (u + v) is also in the subspace.
# Closure under Scalar Multiplication:

# If a vector u is in the subspace, then any scalar multiple (cu) is also in the subspace.
# Example:

# Consider the set of all two-dimensional vectors of the form (x, y) where x and y are real numbers. This set forms a vector space, and we can define a subspace within it.

# Example of a Vector Subspace:

# Subspace U = {(x, y) | x + y = 0}

# Explanation:

# Closure under Vector Addition:

# If (x₁, y₁) and (x₂, y₂) are in U, then (x₁ + x₂, y₁ + y₂) must also be in U.
# For example, if (1, -1) and (-2, 2) are in U, their sum (1 - 2, -1 + 2) = (-1, 1) is also in U because (-1) + 1 = 0.
# Closure under Scalar Multiplication:

# If (x, y) is in U, then c * (x, y) must also be in U for any scalar c.
# For example, if (2, -2) is in U, then 3 * (2, -2) = (6, -6) is also in U because 6 + (-6) = 0.
# So, U is a subspace of the two-dimensional vector space, forming a line where all vectors satisfy the condition x + y = 0.

